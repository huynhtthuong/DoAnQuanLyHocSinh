    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // TODO add your handling code here:
        LopBUS lopBus = new LopBUS();
        String thongTin = txThongTinLop.getText();
        if (thongTin.equals("Thông tin mã lớp")) {
            JOptionPane.showMessageDialog(null, "Hãy nhập thông tin cần tìm");
        }
        else {

            ArrayList<LopDTO> danhSachKqTimDuoc = new ArrayList<LopDTO>();
            danhSachKqTimDuoc = lopBus.timKiemMalop(thongTin);
            if (danhSachKqTimDuoc != null) {
                Vector header = new Vector();
                header.add("Mã lớp");
                header.add("Tên lớp");
                header.add("Phòng");
                header.add("GVCN");
                header.add("Năm học");
                DefaultTableModel model = new DefaultTableModel(header, 0);
                for (LopDTO lop : danhSachKqTimDuoc) {
                    Vector row = new Vector();
                    row.add(lop.getMalop());
                    row.add(lop.getTenlop());
                    row.add(lop.getMaphong().getTenphong());
                    row.add(lop.getMaGVCN().getMagv());
                    row.add(lop.getSiso());
                    row.add(lop.getManh().getTennh());
                    model.addRow(row);
                }
                tbLop.setModel(model);
            }
            else {
                JOptionPane.showMessageDialog(null, "Không tìm thấy lớp với thông tin trên.");
            }
        }
        
    }//GEN-LAST:event_btnTimActionPerformed

    private void btnChonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChonActionPerformed
        // TODO add your handling code here:
        resetTextField();
        int i = tbLop.getSelectedRow();
        if (i >= 0) {
            setMaLophh(tbLop.getModel().getValueAt(i, 0).toString());
            this.setVisible(false);
            tkb.SetCbxLop(maLophh);
            tkb.setVisible(true);
        }
    }//GEN-LAST:event_btnChonActionPerformed

    private void btnDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocActionPerformed
        // TODO add your handling code here:
        LopBUS lopBus = new LopBUS();
        NamHocBUS nhBus = new NamHocBUS();
        PhongBUS phongBus = new PhongBUS();
        MonHocBUS mhBus = new MonHocBUS();
        GiaoVienBUS gvBus = new GiaoVienBUS();
        if (lopBus.getDslop() == null) {
            try {
                nhBus.doc();
                mhBus.docDSMH();
                gvBus.docDSGV(mhBus.getDsmh());
                phongBus.doc();
                lopBus.doc(nhBus.getDsnh(),gvBus.getDsgv(),phongBus.getDsphong());
            } catch (Exception ex) {
                Logger.getLogger(SubFrameChonLop_TKB.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        Vector header = new Vector();
        header.add("Mã lớp");
        header.add("Tên lớp");
        header.add("Phòng");
        header.add("GVCN");
        header.add("Sỉ số");
        header.add("Năm học");
        DefaultTableModel model = new DefaultTableModel(header, 0);
        for (LopDTO lop : lopBus.getDslop()) {
            Vector row = new Vector();
            row.add(lop.getMalop());
            row.add(lop.getTenlop());
            row.add(lop.getMaphong().getTenphong());
            row.add(lop.getMaGVCN().getMagv());
            row.add(lop.getSiso());
            row.add(lop.getManh().getTennh());
            model.addRow(row);
        }
        tbLop.setModel(model);
    }//GEN-LAST:event_btnDocActionPerformed

