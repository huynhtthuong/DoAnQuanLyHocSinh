    private String chuanHoa(String chuoiDauVao) {
        String chuoiBoKhoangTrang = chuoiDauVao.trim();
        String[] arr = chuoiBoKhoangTrang.split(" ");
        StringBuffer sb = new StringBuffer();

        for (int i = 0; i < arr.length; i++) {
            //System.out.println(arr[i]);
            if (arr[i].equals("")) {
                continue;
                
                
            }
            //if (!arr[i].equals("")){
            //System.out.println(arr[i]);
            sb.append(Character.toUpperCase(arr[i].charAt(0))).append(arr[i].substring(1)).append(" ");
            //}
        }
        return sb.toString().trim();
    }
    
    private void showTable(ArrayList<MonHocDTO> danhSachMonHoc) {
        Vector header = new Vector();
        
        header.add("STT");
        header.add("Mã Môn Học");
        header.add("Tên Môn Học");
        header.add("Hệ Số");
        DefaultTableModel model = new DefaultTableModel(header, 0);
        int stt = 1;
        for (MonHocDTO mh: danhSachMonHoc) {
            Vector row = new Vector();
            row.add(stt);
            row.add(mh.getMamh());
            row.add(mh.getTenmh());
            row.add(mh.getHeso());
            model.addRow(row);
            stt++;
        }
        tbDanhSachMonHoc.setModel(model);
        lbSoKetQua.setText("Tìm thấy " + model.getRowCount() + " kết quả.");
    }
    private void resetAllField() {
        txMa.setText("");
        txTen.setText("");
        cbxHeSo.setSelectedItem(0);
    }
    private Boolean kiemTraDauVao() {
        Boolean dauVao = true;
        String thongBao = "";
        int sttThongBao = 1;
        
        if (txMa.getText().length() != MA_LENGTH) {
            dauVao = false;
            thongBao += sttThongBao + ".Mã môn học phải có 1 kí tự \n" ;
            sttThongBao++;
        }
        
        if (txTen.getText().length() > TEN_LENGTH) {
            dauVao = false;
            thongBao += sttThongBao + ". Tối đa 45 ký tự.\n";
            sttThongBao++;
        }
        if (chuanHoa(txTen.getText()).equals("")) {
            dauVao = false;
            thongBao += sttThongBao + ". Họ không được bỏ trống.\n";
            sttThongBao++;
        } 
        
        if (cbxHeSo.getSelectedIndex() == 0){
            thongBao += sttThongBao + ". Vui lòng chọn phái chọn hệ số.\n";
            sttThongBao++;
        }
        if (!thongBao.equals(""))
            JOptionPane.showMessageDialog(null, thongBao);
        return dauVao;
    }
    private void chonMonHoc(){
        int i = tbDanhSachMonHoc.getSelectedRow();
        if (i >= 0) {
            txMa.setText(tbDanhSachMonHoc.getModel().getValueAt(i, 1).toString());
            txTen.setText(tbDanhSachMonHoc.getModel().getValueAt(i, 2).toString());
            if (tbDanhSachMonHoc.getModel().getValueAt(i, 3).toString().equals("1.0")){
                cbxHeSo.setSelectedIndex(1);
            }
            if (tbDanhSachMonHoc.getModel().getValueAt(i, 3).toString().equals("2.0")){
                cbxHeSo.setSelectedIndex(2);
            }
        }
    }
    
    
