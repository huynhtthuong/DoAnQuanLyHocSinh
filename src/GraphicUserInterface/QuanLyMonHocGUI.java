    private void them() {
        Boolean dauVao = kiemTraDauVao();
        Boolean trungMa = mhBus.kiemTraTrungMa(chuanHoa(txMa.getText().toUpperCase()));
        
        if (trungMa)
            JOptionPane.showMessageDialog(null, "Mã môn học bị trùng. Hãy đổi mã môn học khác.");
        if (dauVao && !trungMa) {
            MonHocDTO mhMoi = new MonHocDTO();
            mhMoi.setMamh(chuanHoa(txMa.getText().toUpperCase()));
            mhMoi.setTenmh(chuanHoa(txTen.getText()));
            if(cbxHeSo.getSelectedIndex()==1)
                mhMoi.setHeso(1);
            if(cbxHeSo.getSelectedIndex()==2)
                mhMoi.setHeso(2);
            if(cbxHeSo.getSelectedIndex()==3)
                mhMoi.setHeso(3);
        
            DefaultTableModel model = (DefaultTableModel) tbDanhSachMonHoc.getModel();
            Vector row = new Vector();
            row.add(model.getRowCount() + 1);
            row.add(mhMoi.getMamh());
            row.add(mhMoi.getTenmh());
            row.add(mhMoi.getHeso());
            model.addRow(row);        
        
            tbDanhSachMonHoc.setModel(model);
            lbSoKetQua.setText("Tìm thấy " + model.getRowCount() + " kết quả.");
            resetAllField();

            try {
                mhBus.them(mhMoi);
            } catch (Exception ex) {
                Logger.getLogger(QuanLyMonHocGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

