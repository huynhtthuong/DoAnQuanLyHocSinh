    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // TODO add your handling code here:
        GiaoVienBUS gvBus = new GiaoVienBUS();
        String thongTin = txThongTinGiaoVien.getText();
        if (thongTin.equals("Thông tin mã giáo viên")) {
            JOptionPane.showMessageDialog(null, "Hãy nhập thông tin cần tìm");
        }
        else {

            ArrayList<GiaoVienDTO> danhSachKqTimDuoc = new ArrayList<GiaoVienDTO>();
            danhSachKqTimDuoc = gvBus.timKiemTheoMa(thongTin);
            if (danhSachKqTimDuoc != null) {
                Vector header = new Vector();
                header.add("Mã giáo viên");
                header.add("Họ");
                header.add("Tên");
                header.add("Môn phụ trách");
                DefaultTableModel model = new DefaultTableModel(header, 0);
                for (GiaoVienDTO gv : danhSachKqTimDuoc) {
                    Vector row = new Vector();
                    row.add(gv.getMagv());
                    row.add(gv.getHo());
                    row.add(gv.getTen());
                    row.add(gv.getMonPhuTrach().getTenmh());
                    model.addRow(row);
                }
                tbGiaoVien.setModel(model);
            }
            else {
                JOptionPane.showMessageDialog(null, "Không tìm thấy giáo viên với thông tin trên.");
            }
        }

    }//GEN-LAST:event_btnTimActionPerformed

    private void btnChonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChonActionPerformed
        // TODO add your handling code here:
        resetTextField();
        int i = tbGiaoVien.getSelectedRow();
        if (i >= 0) {
            setMaGiaoVienhh(tbGiaoVien.getModel().getValueAt(i, 0).toString());
            this.setVisible(false);
            pcgd.SetCbxGiaoVien(maGiaoVienhh);
            pcgd.setVisible(true);
        }
    }//GEN-LAST:event_btnChonActionPerformed

    private void btnDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocActionPerformed
        // TODO add your handling code here:
        GiaoVienBUS gvBus = new GiaoVienBUS();
        MonHocBUS mhBus = new MonHocBUS();
        if (gvBus.getDsgv() == null) {
            try {
                mhBus.docDSMH();
                gvBus.docDSGV(mhBus.getDsmh());
            } catch (Exception ex) {
                Logger.getLogger(SubFrameChonGiaoVien.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        Vector header = new Vector();
        header.add("Mã giáo viên");
        header.add("Họ");
        header.add("Tên");
        header.add("Môn phụ trách");
        DefaultTableModel model = new DefaultTableModel(header, 0);
        for (GiaoVienDTO gv : gvBus.getDsgv()) {
            Vector row = new Vector();
            row.add(gv.getMagv());
            row.add(gv.getHo());
            row.add(gv.getTen());
            row.add(gv.getMonPhuTrach().getTenmh());
            model.addRow(row);
        }
        tbGiaoVien.setModel(model);
    }//GEN-LAST:event_btnDocActionPerformed

