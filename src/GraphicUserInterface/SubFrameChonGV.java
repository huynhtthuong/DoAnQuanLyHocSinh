    private void timKiem(){
        ArrayList<GiaoVienDTO> danhSachGiaoVienTimThay = gvBus.getDsgv();
        
        String maCanTim = (String) cbxGiaoVien.getSelectedItem();
        if (!maCanTim.equals(""))
            danhSachGiaoVienTimThay = gvBus.timKiemTheoMa(maCanTim,danhSachGiaoVienTimThay);
        
        String hoCanTim = TienIch.chuanHoa(txHo.getText());
        if (!hoCanTim.equals(""))
            danhSachGiaoVienTimThay = gvBus.timKiemTheoHo(hoCanTim, danhSachGiaoVienTimThay);
        
        String tenCanTim = TienIch.chuanHoa(txTen.getText());
        if (!tenCanTim.equals(""))
            danhSachGiaoVienTimThay = gvBus.timKiemTheoTen(tenCanTim, danhSachGiaoVienTimThay);
        
        
        String monPhuTrach = TienIch.chuanHoa(txMonPhuTrach.getText());
        
        if(!monPhuTrach.equals(""))
            danhSachGiaoVienTimThay = gvBus.timKiemTheoMon(monPhuTrach, danhSachGiaoVienTimThay);
        
        showTable(danhSachGiaoVienTimThay);
    }
    private void showTable(ArrayList<GiaoVienDTO> dsgv){
        Vector header = new Vector();
        header.add("Mã giáo viên");
        header.add("Họ");
        header.add("Tên");
        header.add("Môn phụ trách");
        DefaultTableModel model = new DefaultTableModel(header, 0);
        for (GiaoVienDTO gv : dsgv) {
            Vector row = new Vector();
            row.add(gv.getMagv());
            row.add(gv.getHo());
            row.add(gv.getTen());
            row.add(gv.getMonPhuTrach().getTenmh());
            model.addRow(row);
        }
        tbGiaoVien.setModel(model);
    }
