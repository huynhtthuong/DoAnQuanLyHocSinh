    private void chonMon(){
         int row = tbMonHoc.getSelectedRow();
        if (row >= 0){
            String maMon = (String) tbMonHoc.getModel().getValueAt(row, 0);
            String tenMon = (String) tbMonHoc.getModel().getValueAt(row, 1);
            String heSo = String.valueOf(tbMonHoc.getModel().getValueAt(row, 2)) ;
            
            cbxMon.setSelectedItem(maMon);
            txTenMon.setText(tenMon);
            cbxHeSo.setSelectedItem(heSo);
        }
    }
    
    private void timKiem(){
        ArrayList<MonHocDTO> danhSachLopTimThay= mhBus.getDsmh();
        
        String maMon = (String) cbxMon.getSelectedItem();
        String tenMon = TienIch.chuanHoa(txTenMon.getText());
        String heSo = String.valueOf(cbxHeSo.getSelectedItem());
        
        if (!maMon.equals(""))
            danhSachLopTimThay = mhBus.timKiemTheoMa(maMon, danhSachLopTimThay);
        
        
        if (!tenMon.equals(""))
            danhSachLopTimThay = mhBus.timKiemTheoTenmh(tenMon, danhSachLopTimThay);
        
        
        if (!heSo.equals(""))
            danhSachLopTimThay = mhBus.timKiemTheoHeSo(Integer.parseInt(heSo), danhSachLopTimThay);
        
        showTable(danhSachLopTimThay);
    }
    
    private void showTable(ArrayList<MonHocDTO> dsmh){
        Vector header = new Vector();
        header.add("Mã môn");
        header.add("Tên môn");
        header.add("Hệ số");
        DefaultTableModel model = new DefaultTableModel(header, 0);
        for (MonHocDTO mh : dsmh) {
            Vector row = new Vector();
            row.add(mh.getMamh());
            row.add(mh.getTenmh());
            row.add(mh.getHeso());
            model.addRow(row);
        }
        tbMonHoc.setModel(model);
    }
